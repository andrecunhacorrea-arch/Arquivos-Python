#exercício de tupla, preciso criar uma tupla com 5 nomes e depois exibir esses nomes na tela, com a quantidade total de nomes.

nomes = ("adriano", "josefino", "richardecleison", "eacarlet", "mariana") #criação de uma tupla com 5 nomes.

print("\nnomes das pessoas da lista:")# menssagenm indicando o início da exibição dos nomes.

print(f"o primeiro nome é: {nomes[0]}") #exibe o primeiro nome da tupla.
print(f"o ultimo nome da lista é: {nomes[-1]}") #exibe o último nome da tupla.

print(f"a quantidade total de nomes na lista é: {len(nomes)}") #exibe a quantidade total de nomes na tupla usando a função len().

